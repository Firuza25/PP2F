#Если не указывать начальное значение, диапазон будет начинаться с первого элемента:
thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[:4])

#Это вернет элементы из индекса 0 в индекс 4.



#Если не указывать конечное значение, диапазон перейдет в конец списка
thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[2:])