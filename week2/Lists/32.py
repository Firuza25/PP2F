#Использование цикла While
thislist = ["apple", "banana", "cherry"]
i = 0
while i < len(thislist):
  print(thislist[i])
  i = i + 1#Не забываем увеличивать индекс на 1 после каждой итерации.
  
#Используем функцию len(), чтобы определить длину списка, 
# затем начинаем с 0 и зацикливаемся на элементах списка, ссылаясь на их индексы.

